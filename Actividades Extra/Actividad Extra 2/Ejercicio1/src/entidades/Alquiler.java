package entidades;

import java.util.Date;

public class Alquiler {
    Pelicula peliculaAlquilada;
    Date fechaInicio;
    Date fechaFin;
    double precio;

    public Alquiler(Pelicula peliculaAlquilada, Date fechaInicio, Date fechaFin, double precio) {
        this.peliculaAlquilada = peliculaAlquilada;
        this.fechaInicio = fechaInicio;
        this.fechaFin = fechaFin;
        this.precio = precio;
    }

    public Pelicula getPeliculaAlquilada() {
        return peliculaAlquilada;
    }

    public void setPeliculaAlquilada(Pelicula peliculaAlquilada) {
        this.peliculaAlquilada = peliculaAlquilada;
    }

    public Date getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(Date fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public Date getFechaFin() {
        return fechaFin;
    }

    public void setFechaFin(Date fechaFin) {
        this.fechaFin = fechaFin;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    @Override
    public String toString() {
        return "Alquiler{" +
                "\npeliculaAlquilada=" + peliculaAlquilada +
                "\nfechaInicio=" + fechaInicio +
                "\nfechaFin=" + fechaFin +
                "\nprecio=" + precio +
                '}';
    }
}
